package sample.Ex007;

public class Medal {

    private String name;
    private int price;

    public Medal(String name,int price){
        this.name=name;
        this.price=price;

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }



}
