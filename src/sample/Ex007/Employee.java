package sample.Ex007;

public class Employee {

    private String name;
    private int salay;

    public Employee(String name,int salary){
        this.name=name;
        this.salay=salary;

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSalay() {
        return salay;
    }

    public void setSalay(int salay) {
        this.salay = salay;
    }



    



}
